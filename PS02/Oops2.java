public class Oops2 {
    public static void main(String[] args) {
        int x = 0;
        System.out.println("x is " + x);

        int z = 15;   // set x to 15.2
        System.out.println("x is now " + z);

        int y = z + 1;   // set y to 1 more than
        System.out.println("x and y are " + z + " and " + y);
    }
}